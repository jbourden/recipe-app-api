'''
Calculator functions
'''


def add(x, y):
    '''Add x and y and return result'''


    return x + y

def subtract(x, y):
    '''Add x and y and return result'''


    return x - y

def multiply(x, y):
    '''Add x and y and return result'''


    return x * y

def divide(x, y):
    '''Add x and y and return result'''


    return x // y
